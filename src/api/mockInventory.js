  // Get all inventory items with optional filtering and pagination
  async getInventory({
    page = 1,
    perPage = 10,
    search = "",
    category = "",
    stockFilter = "all",
  } = {}) {
    await new Promise((resolve) => setTimeout(resolve, 300)); // Simulate API delay

    let filteredItems = [...inventoryItems];

    // Apply search filter
    if (search) {
      const searchLower = search.toLowerCase();
      filteredItems = filteredItems.filter(
        (item) =>
          item.nombre.toLowerCase().includes(searchLower) ||
          String(item.sku).toLowerCase().includes(searchLower) // Convertir a string
      );
    }

    // Apply category filter
    if (category) {
      filteredItems = filteredItems.filter(
        (item) => item.categoria === category
      );
    }

    // Apply stock filter
    if (stockFilter !== "all") {
      filteredItems = filteredItems.filter((item) => {
        if (stockFilter === "low") {
          return item.stock <= item.stockMinimo;
        } else if (stockFilter === "out") {
          return item.stock === 0;
        }
        return true;
      });
    }

    // Calculate pagination
    const total = filteredItems.length;
    const start = (page - 1) * perPage;
    const items = filteredItems.slice(start, start + perPage);

    return {
      data: {
        items,
        pagination: {
          total,
          page,
          perPage,
          totalPages: Math.ceil(total / perPage),
        },
      },
    };
  },

  // Get categories
